import mongoose from "mongoose";
const contactschema = mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
  },
  message: {
    type: String,
    required: true,
    trim: true,
  },
});
const contactUs = mongoose.model("contacts", contactschema);
export default contactUs;
